# Month names for the history trees (Trips / Charges / Statistics / Wallbox).
# Python's strftime("%B"/"%b") is locale-C (always English) unless setlocale is
# called, which is fragile in slim containers — so we localize the labels here.
# "full" = capitalized month header ("%B %Y"); "abbr" = day-label month ("%d %b %Y"),
# kept lowercase for it/fr (reads naturally mid-string) and capitalized for en/de.
_MONTHS: dict[str, dict[str, list[str]]] = {
    "en": {
        "full": ["January", "February", "March", "April", "May", "June",
                 "July", "August", "September", "October", "November", "December"],
        "abbr": ["Jan", "Feb", "Mar", "Apr", "May", "Jun",
                 "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"],
    },
    "it": {
        "full": ["Gennaio", "Febbraio", "Marzo", "Aprile", "Maggio", "Giugno",
                 "Luglio", "Agosto", "Settembre", "Ottobre", "Novembre", "Dicembre"],
        "abbr": ["gen", "feb", "mar", "apr", "mag", "giu",
                 "lug", "ago", "set", "ott", "nov", "dic"],
    },
    "fr": {
        "full": ["Janvier", "Février", "Mars", "Avril", "Mai", "Juin",
                 "Juillet", "Août", "Septembre", "Octobre", "Novembre", "Décembre"],
        "abbr": ["janv", "févr", "mars", "avr", "mai", "juin",
                 "juil", "août", "sept", "oct", "nov", "déc"],
    },
    "de": {
        "full": ["Januar", "Februar", "März", "April", "Mai", "Juni",
                 "Juli", "August", "September", "Oktober", "November", "Dezember"],
        "abbr": ["Jan", "Feb", "Mär", "Apr", "Mai", "Jun",
                 "Jul", "Aug", "Sep", "Okt", "Nov", "Dez"],
    },
}


def fmt_month_year(lang: str, dt) -> str:
    """Localized "%B %Y" → e.g. "Giugno 2026". Used for month headers in history trees."""
    months = _MONTHS.get(lang, _MONTHS["en"])
    return f"{months['full'][dt.month - 1]} {dt.year}"


def fmt_day_month_year(lang: str, dt) -> str:
    """Localized "%d %b %Y" → e.g. "02 giu 2026". Used for day labels in history trees."""
    months = _MONTHS.get(lang, _MONTHS["en"])
    return f"{dt.day:02d} {months['abbr'][dt.month - 1]} {dt.year}"


def get_t(lang: str):
    strings = _T.get(lang, _T["en"])
    fallback = _T["en"]
    def t(key: str) -> str:
        return strings.get(key, fallback.get(key, key))
    return t
